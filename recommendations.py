def get_recommendation(factor: str) -> str:
    """Повертає рекомендацію для фактора або загальну фразу."""
    rec = RECOMMENDATIONS.get(factor)
    if rec:
        return rec
    return (
        "Цей елемент не знайдено автоматично. "
        "Перевірте вручну та додайте відповідний контент або розмітку на сайт."
    )
